//push method is used for adding items in the array
let fruit = ["apple", "banana"]
console.log(fruit)

fruit.push("mango");
console.log(fruit);

fruit.push("orange");
console.log(fruit);

//pop is used for removed items from the array
fruit.pop()
fruit.pop()
console.log(fruit);

let color = ["red", "blue", "pink"]
console.log(color);

let removeColor = color.pop()
console.log(removeColor);